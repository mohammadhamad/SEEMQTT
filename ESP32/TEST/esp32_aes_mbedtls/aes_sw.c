/*
* /brief subtitute every byte with the corresponding one in LUT
* /para *buffer - actual message to be subtituted
* /return void
*/
void SubBytes_128(uint8_t *buffer)
{
	//substitute every byte
	for(uint8_t i = 0; i < 16; i++)
 		buffer[i] = LUT[buffer[i]];
}

 /*
 * /brief subtitute every byte with the corresponding one in LUT
 * /para *buffer - actual message to be subtituted
 * /return void
 */
 void SubBytesInverse_128(uint8_t *buffer)
 {
 	//substitute every byte
 	for(uint8_t i = 0; i < 16; i++)
 		buffer[i] = LUT_INVERSE[buffer[i]];
 }

/*
* /brief shift rows of the input matrix, with the offset 0, 1, 2, 3
* /para *buffer - actual message to be shifted
* /return void
*/
void ShiftRows_128(uint8_t *buffer)
{
	//converrt the 8bit pointer to 32bit
	uint32_t *temp_buffer = (uint32_t *)buffer;

	//shift second row with offset 1 byte
	temp_buffer[1] = (temp_buffer[1] >> 8) + (temp_buffer[1] << 24);
	//shift third row with offset 2 byte
	temp_buffer[2] = (temp_buffer[2] >> 16) + (temp_buffer[2] << 16);
	//shift fourth row with offset 3 byte
	temp_buffer[3] = (temp_buffer[3] >> 24) + (temp_buffer[3] << 8);
}

 /*
 * /brief shift rows of the input matrix, with the offset 0, 3, 2, 1
 * /para *buffer - actual message to be shifted
 * /return void
 */
 void ShiftRowsInverse_128(uint8_t *buf)
 {
	//converrt the 8bit pointer to 32bit
	uint32_t *temp_buffer = (uint32_t *)buf;

	//shift second row with offset 3 byte
	temp_buffer[1] = (temp_buffer[1] >> 24) + (temp_buffer[1] << 8);
	//shift third row with offset 2 byte
	temp_buffer[2] = (temp_buffer[2] >> 16) + (temp_buffer[2] << 16);
	//shift fourth row with offset 1 byte
	temp_buffer[3] = (temp_buffer[3] >> 8) + (temp_buffer[3] << 24);
 }

 // The following xtime_E, xtime_B, xtime_D, xtime_9 are derived using xtime()
 // reference: https://www.usenix.org/legacy/publications/library/proceedings/cardis02/full_papers/valverde/valverde_html/node15.html
 uint8_t xtime_E(uint8_t in_data)
 {
 	uint8_t out_data = 0;

 	uint8_t temp_xtime = xtime(in_data);
 	uint8_t temp_xtime_2 = xtime(temp_xtime);
 	uint8_t temp_xtime_3 = xtime(temp_xtime_2);

 	out_data = temp_xtime ^ temp_xtime_2 ^ temp_xtime_3;

 	return out_data;
 }

